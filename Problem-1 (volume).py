# A program for finding the volume of a box
def main():
    print("This Program calculates volume of a box from user inputs.")
    l = int(input("The value for length: "))
    w =  int(input("The value for width: "))
    h =  int(input("The value for height: "))
    volume = l * w * h
    print("The volume of the box is: ",int(volume))
main()

