# A program that converts kilometers to miles.
def main():
    print("A program that converts kilometers to miles.")
    kilometer= float(input("Enter the value in Kilometer: "))
    miles = kilometer*0.62
    print("The converted value is:",round(miles,2),"Miles")
main()
